const mongoose = require('mongoose');

const RegisterSchema = new mongoose.Schema({
    username: {
        type: String,
        required: true,
    },
    email: {
        type: String,
        required: true,
    },
    phone: {
        type: String,
        required: true,
    },
    address: {
        type: String,
        required: true,
    }
});

const Register = mongoose.model('Register', RegisterSchema)
module.exports = Register